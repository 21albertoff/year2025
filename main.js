  var contadorErrores = 0;
  var contadorTotalErrores = 0;
  
  var ultimoCodigoCorrecto = true;

  function validarCodigo() {
    limpiarResultado();

    var codigoUsuario = document.getElementById("codigo").value.toUpperCase();

    if (codigoUsuario === "") {
      return;
    }

    if (codigoUsuario in codigos) {
      if (!ultimoCodigoCorrecto) {
        contadorErrores = 0;
        document.getElementById("codigoErroneo").innerText = "";
      }
      ultimoCodigoCorrecto = true;

      var resultadoDiv = document.getElementById("resultado");
      resultadoDiv.style.display = "flex";
      var texto = codigos[codigoUsuario];
      resultadoDiv.innerHTML = "<pre style='font-size: 0.64rem; font-weight: 100; white-space: pre-wrap; word-wrap: break-word;'>" + texto + "</pre>";

      document.getElementById("botonMenuPrincipal").style.display =
        "inline-block";

      document.getElementById("formulario").style.display = "none";
    } else {
      ultimoCodigoCorrecto = false;
      contadorErrores++;
      contadorTotalErrores++;
      mostrarContadorErrores(contadorErrores);
      document.getElementById("codigoErrores").style.display = "none";
      var codigoErroneoDiv = document.getElementById("codigoErroneo");
      var mensaje = "¡Código Erróneo! ";
      if (contadorErrores > 1) {
        mensaje += contadorErrores + " Errores";

        if (contadorErrores == '3') {
            mensaje += "\nEres malisima";
          }

        if (contadorErrores == '9') {
          mensaje += "\n¿Que quieres llegar a los 33 errores?";
        }

        if (contadorErrores == '12') {
          mensaje += "\nPrueba con algo simple, facil, lo primero que te venga a la cabeza";
        }

        if (contadorErrores == '16') {
          mensaje += "\n¿Tan mala eres que no atinas ni una?";
        }

        if (contadorErrores == '30') {
          mensaje += "\nFalla 3 veces mas y te digo una pista";
        }

        if (contadorErrores == '33') {
          mensaje += "\n¿Como te llamas?";
        }

        if (contadorErrores > '40' && contadorErrores < '60') {
          mensaje += "\nNo continues, lo tuyo no tiene solución.";
        }
      }
      codigoErroneoDiv.innerText = mensaje;
    }
  }

  function limpiarResultado() {
    var resultadoDiv = document.getElementById("resultado");
    resultadoDiv.innerHTML = "";
  }

  function mostrarContadorErrores(contador) {
    var contadorDiv = document.getElementById("codigoErroneo");
    contadorDiv.innerText = "¡Código Erróneo! " + contador + " Errores";
  }

  function irAlMenu() {
    document.getElementById("formulario").style.display = "block";
    document.getElementById("resultado").style.display = "none";
    document.getElementById("botonMenuPrincipal").style.display = "none";
  }

